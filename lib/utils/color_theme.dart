import 'dart:ui';

class ColorTheme {
  final Color primary;
  final Color secondary;
  final Color tertiary;
  final Color light;
  final Color lighter;
  final Color lightest;
  final Color background;
  final Color backgroundLight;
  final Color backgroundLighter;
  final Color textOnPrimary;
  final Color textOnSecondary;
  final Color textOnTertiary;
  final Color textOnBackground;

  ColorTheme({
    required this.primary,
    required this.secondary,
    required this.tertiary,
    required this.light,
    required this.lighter,
    required this.lightest,
    required this.background,
    required this.backgroundLight,
    required this.backgroundLighter,
    required this.textOnPrimary,
    required this.textOnSecondary,
    required this.textOnTertiary,
    required this.textOnBackground,
  });
}
